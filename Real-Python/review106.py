# Declare a tuple. Immutable but can be indexed the same as a list"
cardinal_nums = 'first', 'second', 'third'

# Prints second object in tuple.
print(cardinal_nums[1])

# Unpack tuples by assigning each position to a string value.
pos1, pos2, pos3 = cardinal_nums
print(pos1)
print(pos2)
print(pos3)
