# Create string object with integer as value then convert it into an integer
stringToInt = "2"
print(stringToInt + stringToInt)
stringToInt = int(stringToInt)
print(stringToInt + stringToInt)

# Repeat the previous code with floating point number instead of integer
stringToFloat = "10"
print(stringToFloat)
stringToFloat = float(stringToFloat)
print(stringToFloat)

